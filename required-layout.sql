CREATE TABLE CREDIT_CARDS
(
    id                  NUMBER(10) GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL,
    card_id             NUMBER(10) NOT NULL,                          -- id of card in users_cards table
    state               VARCHAR2(255) DEFAULT 'can_request' NOT NULL, -- state of credit processing|can_request|not_approved|paid|in_grace_period|overdue
    credit_limit        NUMBER(10) DEFAULT 0 NOT NULL,                -- credit limit
    billing_cycle       NUMBER(10) DEFAULT 0 NOT NULL,                -- billing cycle
    next_statement_date TIMESTAMP NULL,                               -- next statement date
    payment_due_date    TIMESTAMP NULL,                               -- payment due date
    grace_period        NUMBER(10) DEFAULT 45 NOT NULL,               -- grace period in days
    minimal_payment     NUMBER(10) DEFAULT 0 NOT NULL,                -- minimal payment
    total_payment       NUMBER(10) DEFAULT 0 NOT NULL,                -- total payment
    meta                CLOB NULL,                                    -- meta information
    created_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT credit_cards_pk PRIMARY KEY (id)
);
create index CREDIT_CARDS_CARD_ID on CREDIT_CARDS (card_id);

